    private void submitJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitJButtonActionPerformed
       
         String name = nameJTextField.getText();
        Validation val = new Validation();
        boolean a = val.regexTextCheck(nameJTextField.getText());
        if (nameJTextField.getText().trim().equalsIgnoreCase("")) {
            JOptionPane.showMessageDialog(null, "Please enter valid name!");
            return;
        }
        if (!a) {
            JOptionPane.showMessageDialog(null, "Please enter networks in alphabets only");
            return;
        } else {
            boolean net = business.checkIfNetworkIsUnique(name);
            if (net) {
                JOptionPane.showMessageDialog(null, " Network already exist");

           } else {

               Network network = business.createAndAddNetwork();
                network.setName(name);

               populateNetworkTable();
                nameJTextField.setText("");
            }
        }
        
        
    }//GEN-LAST:event_submitJButtonActionPerformed

