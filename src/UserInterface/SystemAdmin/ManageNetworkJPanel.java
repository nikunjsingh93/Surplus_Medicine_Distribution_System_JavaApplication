    private void removeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_removeActionPerformed
        // TODO add your handling code here:
        
         int selectedRow = networkJTable.getSelectedRow();
        if (selectedRow < 0){
            JOptionPane.showMessageDialog(null, "Please select a Network to remove");
        }
        
         Network network = (Network) networkJTable.getValueAt(selectedRow, 0);
        
         business.removeNetwork(network);
         
         populateNetworkTable();
         
        
         
         JOptionPane.showMessageDialog(null, network +" network removed");
         
          nameJTextField.setText("");
    }//GEN-LAST:event_removeActionPerformed


