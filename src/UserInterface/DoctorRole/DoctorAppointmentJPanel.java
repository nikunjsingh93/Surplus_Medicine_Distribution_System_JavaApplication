    private void populateData() {
        
        for (WorkRequest wr : organization.getWorkQueue().getWorkRequestList()) {
            textAge.setText(String.valueOf(wr.getSender().getPerson().getAge()));
            textFirstName.setText(wr.getSender().getPerson().getFirstName());
            textLastName.setText(wr.getSender().getPerson().getLastName());
            textGender.setText(wr.getSender().getPerson().getGender());
            textTime.setText(((PatientDoctorWorkRequest) request).getTime());
            break;
        }
    }
