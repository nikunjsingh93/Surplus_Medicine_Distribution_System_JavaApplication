    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed
        
        if (textPrescibedMedicine.getText().equals("") || textPrescibedMedicine.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Prescribed medicine cannot be blank");
            return;
        }
        if (textDisease.getText().equals("") || textDisease.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Disease name cannot be blank");
            return;
        }
        if (textQty.getText().equals("") || textQty.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Quantity cannot be blank");
            return;
        }

        Validation v = new Validation();
        boolean a = v.regexMedicineName(textPrescibedMedicine.getText());
        boolean b = v.regexTextCheck(textDisease.getText());
        boolean c = v.regexAgeCheck(textQty.getText());

        if (!a) {
            JOptionPane.showMessageDialog(null, "Please enter valid prescribed name");
            return;
        } else {
            if (!b) {
                JOptionPane.showMessageDialog(null, "Please enter valid disease name");
                return;
            } else {
                if (!c) {
                    JOptionPane.showMessageDialog(null, "Please enter valid quantity number");
                    return;
                } else {

                    Person person = request.getSender().getPerson();//organization.getPersonList().createPerson();
                    Doctor doctor = new Doctor();
                    doctor.setDisease(textDisease.getText());
                    doctor.setPrescribedMedicine(textPrescibedMedicine.getText());
                    doctor.setMedicineQty(Integer.parseInt(textQty.getText()));
                    doctor.getPersonDirectory().createPerson(person);
                    organization.getDoctorList().CreateDoctor(doctor);
                    ua.setDoctor(doctor);
                    ((PatientDoctorWorkRequest) request).setTime(textTime.getText());
                    JOptionPane.showMessageDialog(null, "Data saved succesfully");
                }
            }
        }
    

    }//GEN-LAST:event_btnSubmitActionPerformed

